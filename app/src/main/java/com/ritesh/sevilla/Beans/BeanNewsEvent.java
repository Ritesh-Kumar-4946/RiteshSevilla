public class BeanNewsEvent {

    private String EventNews_id;
    private String EventNewsTittle;
    private String EventNewsDescription;
    private String EventNewsDate;
    private String EventNewsImage;

    public String getEventNewsId() {
        return EventNews_id;
    }
    public void setEventNewsId(String EventNews_id) {
        this.EventNews_id = EventNews_id;
    }


    public String getEventNewsTittle() {
        return EventNewsTittle;
    }
    public void setEventNewsTittle(String EventNewsTittle) {
        this.EventNewsTittle = EventNewsTittle;
    }


    public String getEventNewsDescription() {
        return EventNewsDescription;
    }
    public void setEventNewsDescription(String EventNewsDescription) {
        this.EventNewsDescription = EventNewsDescription;
    }


    public String getEventNewsDate() {
        return EventNewsDate;
    }
    public void setEventNewsDate(String EventNewsDate) {
        this.EventNewsDate = EventNewsDate;
    }


    public String getEventNewsImage() {
        return EventNewsImage;
    }
    public void setEventNewsImage(String EventNewsImage) {
        this.EventNewsImage = EventNewsImage;
    }


}
